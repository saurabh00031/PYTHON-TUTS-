
#operators in pythons
#arithmetic operator........................................................................................

v1=2
v2=5

print(v1+v2)
print(v1-v2)
print(v1*v2)
print(v1/v2)        #float
print(v1//v2)       #integer
print(v1**v2)
print(v1%v2)       #modulus.....gives remainder

#assignment operator........................................................................................

sp1=10
sp2=20
sp3=sp1+sp2
sp3=sp3+1
print(sp3)

#comparison operator.........................................................................................

s1,s2=10,20

if s2>s1:
    print("YES")

else:
    print("NO")


#logical operator.............................................................................................

a=True
b=False
print(a  and b)   
print(a  or  b)





#identity operator..............................................................................................

print(a is not b)


#membership operator..............................................................................................

list1=[1,2,3,4,5,6,7]
print(324 in list1)



#bitwise operator..................................................................................................

print(1 | 3)       #or
print(1 & 3)       #and
print(1 ^ 3)       #xor



